CREATE TABLE KHOA(
	MAKHOA VARCHAR(100) PRIMARY KEY NOT NULL,
	TENKHOA NVARCHAR(100) NOT NULL,
	NAMTHANHLAP INT CHECK(NAMTHANHLAP>=0 AND NAMTHANHLAP<=9999) NOT NULL,	
)
GO

CREATE TABLE LOP(
	MALOP VARCHAR(100) PRIMARY KEY NOT NULL,
	TENLOP NVARCHAR(100) NOT NULL,
	SISO INT CHECK(SISO>=20 AND SISO<=100) NOT NULL,
	MAKHOA VARCHAR(100) FOREIGN KEY REFERENCES KHOA(MAKHOA) NOT NULL
)
GO


CREATE TABLE MONHOC
(
	MAMH VARCHAR(100) PRIMARY KEY NOT NULL,
	TENMH NVARCHAR(100) NOT NULL
)
GO

CREATE TABLE HOCPHAN(
	MAHP VARCHAR(100) PRIMARY KEY NOT NULL,
	TENHP NVARCHAR (100) NOT NULL,
	TINCHI INT CHECK(TINCHI<=5 AND TINCHI>=1) NOT NULL,
	HOCKY INT CHECK(HOCKY<=8 AND HOCKY>=0) NULL,
	PTQUATRINH INT NULL,
	MAMH VARCHAR(100) FOREIGN KEY REFERENCES MONHOC(MAMH) NOT NULL
)
GO

CREATE TABLE GIANGVIEN
(
	MAGV VARCHAR(100)  PRIMARY KEY NOT NULL,
	TENGV NVARCHAR(100) NOT NULL,
	GIOITINH NVARCHAR(5) CHECK (GIOITINH IN (N'Nam',N'Nữ') ) NOT NULL,
	DIACHI NVARCHAR(100) NULL,
	CHUCVU NVARCHAR(100) NULL,
	EMAIL NVARCHAR(100) NULL,
	ID VARCHAR(10) FOREIGN KEY REFERENCES TAIKHOAN(ID) 
)
GO

CREATE TABLE SINHVIEN(
	MASV VARCHAR(100) PRIMARY KEY NOT NULL,
	HOTEN NVARCHAR(100) NOT NULL,
	URLANH NVARCHAR(200) NULL,
	NGAYSINH DATE NULL,
	GIOITINH NVARCHAR(5) CHECK (GIOITINH IN (N'Nam',N'Nữ') ) NOT NULL,
	QUEQUAN NVARCHAR(100) NULL,
	CHOOHIENTAI NVARCHAR(100) NULL,
	CHUYENNGANH NVARCHAR(100) NULL,
	DANTOC NVARCHAR(100) NULL,
	MAKHOA VARCHAR(100) FOREIGN KEY REFERENCES KHOA(MAKHOA) NOT NULL,
	MALOP VARCHAR(100) FOREIGN KEY REFERENCES LOP(MALOP) NULL,
	DIEMRENLUYEN INT NULL,
	ID VARCHAR(10) FOREIGN KEY REFERENCES TAIKHOAN(ID) 
)
GO

CREATE TABLE KETQUA
(
	MASV VARCHAR(100) NOT NULL,
	MAMH VARCHAR(100) NOT NULL,
	MAHP VARCHAR(100) NOT NULL,
	DIEMQT FLOAT NOT NULL CHECK (DIEMQT >=0 AND DIEMQT <=10),
	DIEMTHI FLOAT NOT NULL CHECK (DIEMTHI >= 0 AND DIEMTHI <= 10),
	DIEMTK FLOAT NOT NULL CHECK (DIEMTK >= 0 AND DIEMTK <= 10),
	KQ BIT,
	PRIMARY KEY (MASV,MAMH,MAHP),
	FOREIGN KEY (MASV) REFERENCES SINHVIEN(MASV),
	FOREIGN KEY (MAMH) REFERENCES MONHOC(MAMH),
	FOREIGN KEY (MAHP) REFERENCES HOCPHAN(MAHP)
)
GO

